public class DefaultConstructorV3 {
    private String fullName;
    private int age;
    private boolean male;
    private Boolean female;

    public String getFullName() {
        return fullName;
    }

    public int getAge() {
        return age;
    }

    public boolean IsMale() {
        return male;
    }

    public Boolean getFemale() {
        return female;
    }

}
