package main.java.lv.javaguru.teacher.lesson_5_arrays_for_loop.lessoncode;

public class IteratorFor {

    public static void main(String[] args) {

        for (int i = 10; i >= 0; i--) {
            System.out.println("Iteration - " + i + " : Hello");
        }

        System.out.println("Out of for");
    }
}
